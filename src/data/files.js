let emptyFolder = {
  name: 'Dir 4',
  id: 4,
  isDir: true,
  children: [],
  files: []
}

function findById(folder, id) {
  let res = false;
  for (let item of folder.children) {
    if (item.id === id) {
      return item;
    } else {
      if (item.children.length) {
        res = findById(item, id);
        if (res) {
          return res;
        }
      }
    }
  }
  return false;
}

export default function getByFolder(id) {
  if (!id) {
    return all;
  } else {
    let result = findById(all, id);
    return result ? result : emptyFolder ;
  }
}
